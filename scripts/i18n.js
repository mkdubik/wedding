document.addEventListener("DOMContentLoaded", function() {

    const isButton = document.getElementById("is")
    const enButton = document.getElementById("en")
    const plButton = document.getElementById("pl")

    isButton.addEventListener("click", function() {
        if (currentLanguage != "is") {
            isButton.innerHTML = "<u>√çslenska</u>"
            enButton.innerHTML = "English"
            plButton.innerHTML = "Polski"
            translatePage("is")
        }
    });

    enButton.addEventListener("click", function() {
        if (currentLanguage != "en") {
            isButton.innerHTML = "√çslenska"
            enButton.innerHTML = "<u>English</u>"
            plButton.innerHTML = "Polski"
            translatePage("en")
        }
    });

    plButton.addEventListener("click", function() {
        if (currentLanguage != "pl") {
            isButton.innerHTML = "√çslenska"
            enButton.innerHTML = "English"
            plButton.innerHTML = "<u>Polski</u>"
            translatePage("pl")
        }
    });

    const urlParams = new URLSearchParams(window.location.search);
    const lang = urlParams.get('lang')

    switch (lang) {
        case "en":
            isButton.innerHTML = "√çslenska"
            enButton.innerHTML = "<u>English</u>"
            plButton.innerHTML = "Polski"
            currentLanguage = "en"
            break;
        case "pl":
            isButton.innerHTML = "√çslenska"
            enButton.innerHTML = "English"
            plButton.innerHTML = "<u>Polski</u>"
            currentLanguage = "pl"
            break;
        default:
            isButton.innerHTML = "<u>√çslenska</u>"
            enButton.innerHTML = "English"
            plButton.innerHTML = "Polski"
            currentLanguage = "is"
            break
    }

    translatePage(currentLanguage)
});

function translatePage(language) {
    const translate = document.getElementsByClassName("translate");

    for (let i = 0; i < translate.length; i++) {
        if (translate[i].nodeName === "INPUT") {
            translate[i].placeholder = i18n[language][translate[i].name]
        } else {
            translate[i].innerHTML = i18n[language][translate[i].id]
        }
    }

    currentLanguage = language
}

